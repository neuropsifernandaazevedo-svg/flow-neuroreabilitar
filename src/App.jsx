import { useState, useCallback } from "react";

const C = {
  coral:"#E07060", coralL:"#FBF0EE", rosa:"#E8909A", rosaL:"#FDF0F2",
  teal:"#2D7070", tealL:"#E8F4F4", tealM:"#7BBFB5",
  creme:"#FAF6F0", cinza:"#4A4A5A", cinzaM:"#8A8A9A", cinzaL:"#EEECF0",
  branco:"#FFFFFF", verm:"#C0392B", vermL:"#FEE2E2",
};

const hoje = () => new Date().toISOString().slice(0,10);
const fmtData = d => d ? new Date(d+"T12:00:00").toLocaleDateString("pt-BR") : "—";

// ── Gerar boletim localmente (sem IA, sem internet) ───────────────────────────
function gerarBoletimLocal(crianca, ficha) {
  const nome = crianca.nome || "Participante";
  const hab = ficha.objetivo || "habilidades sociais";
  return {
    nome,
    habilidade: `Hoje trabalhamos: ${hab}.`,
    fez_bem: crianca.positivo || `${nome} participou ativamente da sessão.`,
    orientacao: crianca.orientacao || `Continue praticando em casa. ${crianca.atencao ? "Ponto de atenção: " + crianca.atencao : ""}`.trim(),
  };
}

// ── Exportar como texto ───────────────────────────────────────────────────────
function gerarTexto(tipo, ficha, boletins, pauta) {
  if (tipo === "ficha") {
    const c = (ficha.criancas||[]).filter(x=>x.nome?.trim());
    return [
      "FICHA DE SESSÃO — GRUPO FLOW / Neuroreabilitar",
      `Data: ${fmtData(ficha.data)}  |  Facilitadora: ${ficha.facilitadora||"—"}  |  Turma: ${ficha.turma||"—"}  |  Presentes: ${ficha.presentes||"—"}`,
      "", "SESSÃO",
      `Objetivo: ${ficha.objetivo||"—"}`,
      `Recurso: ${ficha.recurso||"—"}`,
      `Discussão: ${ficha.analise||"—"}`,
      `Prática: ${ficha.pratica||"—"}`,
      `Dinâmica: ${ficha.dinamica||"—"}`,
      "", "CRIANÇAS",
      ...c.map(x=>`▸ ${x.nome}\n  Positivo: ${x.positivo||"—"}\n  Atenção: ${x.atencao||"—"}\n  Orientação: ${x.orientacao||"—"}`),
      "", `Próxima sessão: ${ficha.proxima||"—"}`,
      "", `Gerado em: ${new Date().toLocaleString("pt-BR")}`,
    ].join("\n");
  }
  if (tipo === "boletins") {
    if (!boletins.length) return "Nenhum boletim gerado.";
    return boletins.map(b=>[
      `BOLETIM — ${(b.nome||"").toUpperCase()} | Grupo FLOW / Neuroreabilitar | ${fmtData(ficha.data)}`,
      "", `Hoje trabalhamos:\n  ${b.habilidade}`,
      "", `O que ${b.nome} fez bem:\n  ${b.fez_bem}`,
      "", `Para praticar em casa:\n  ${b.orientacao}`,
      "", "Dúvidas? Agende uma conversa — não respondemos por WhatsApp.",
      "─────────────────────────────────────",
    ].join("\n")).join("\n\n");
  }
  if (tipo === "pauta") {
    const c = (ficha.criancas||[]).filter(x=>x.nome?.trim());
    return [
      "PAUTA — ENCONTRO DE PAIS | Grupo FLOW / Neuroreabilitar",
      `Data: ${fmtData(ficha.data)}  |  Turma: ${ficha.turma||"—"}`,
      "", "CONQUISTAS DO GRUPO",
      ficha.dinamica || "—",
      "", "DEVOLUTIVA POR CRIANÇA",
      ...c.map(x=>`▸ ${x.nome}\n  O que fez bem: ${x.positivo||"—"}\n  Atenção: ${x.atencao||"—"}\n  Orientação para casa: ${x.orientacao||"—"}`),
      "", "PRÓXIMO CICLO",
      `Habilidade foco: ${ficha.proxima||"—"}`,
      "", `Gerado em: ${new Date().toLocaleString("pt-BR")}`,
    ].join("\n");
  }
  return "";
}

// ── Componentes ───────────────────────────────────────────────────────────────
function Campo({label, valor, onChange, rows, placeholder, cor=C.coral}) {
  const s = {
    width:"100%", fontFamily:"inherit", fontSize:14, color:C.cinza,
    border:`1.5px solid ${C.cinzaL}`, borderRadius:9, padding:"9px 12px",
    outline:"none", resize:rows?"vertical":"none", lineHeight:1.65,
    background:C.branco, transition:"border 0.15s",
  };
  return (
    <div style={{marginBottom:12}}>
      <div style={{fontSize:11,fontWeight:700,letterSpacing:0.5,color:cor,textTransform:"uppercase",marginBottom:5}}>{label}</div>
      {rows
        ? <textarea rows={rows} value={valor||""} onChange={e=>onChange(e.target.value)} placeholder={placeholder} style={s}
            onFocus={e=>e.target.style.border=`1.5px solid ${cor}`}
            onBlur={e=>e.target.style.border=`1.5px solid ${C.cinzaL}`} />
        : <input value={valor||""} onChange={e=>onChange(e.target.value)} placeholder={placeholder} style={s}
            onFocus={e=>e.target.style.border=`1.5px solid ${cor}`}
            onBlur={e=>e.target.style.border=`1.5px solid ${C.cinzaL}`} />
      }
    </div>
  );
}

function Bloco({titulo, cor, corL, children}) {
  return (
    <div style={{marginBottom:14,borderRadius:14,overflow:"hidden",border:`1px solid ${cor}33`,boxShadow:"0 1px 6px rgba(0,0,0,0.04)"}}>
      <div style={{background:cor,color:C.branco,padding:"10px 16px",fontWeight:700,fontSize:13}}>{titulo}</div>
      <div style={{background:corL,padding:"14px 16px 6px"}}>{children}</div>
    </div>
  );
}

function Pill({label, sel, onClick, cor}) {
  return (
    <button onClick={onClick} style={{
      padding:"5px 13px",borderRadius:20,fontSize:12,fontFamily:"inherit",
      fontWeight:sel?700:400,cursor:"pointer",
      border:`1.5px solid ${sel?cor:C.cinzaL}`,
      background:sel?cor:C.branco, color:sel?C.branco:C.cinza,
      transition:"all 0.15s",
    }}>{label}</button>
  );
}

function Toast({msg}) {
  if (!msg) return null;
  return (
    <div style={{
      position:"fixed",bottom:82,left:"50%",transform:"translateX(-50%)",
      background:C.teal,color:C.branco,padding:"9px 24px",
      borderRadius:24,fontSize:13,fontWeight:600,
      boxShadow:"0 4px 20px rgba(0,0,0,0.2)",zIndex:300,pointerEvents:"none",whiteSpace:"nowrap",
    }}>{msg}</div>
  );
}

// ── Card criança ──────────────────────────────────────────────────────────────
function CardCrianca({idx, dados, onChange, onRemover}) {
  const u = (k,v) => onChange({...dados,[k]:v});
  const tem = dados.nome?.trim();
  return (
    <div style={{background:C.branco,borderRadius:12,border:`1.5px solid ${tem?C.coral+"55":C.cinzaL}`,padding:"11px 13px",marginBottom:9,transition:"border 0.2s"}}>
      <div style={{display:"flex",gap:10,alignItems:"center",marginBottom:tem?10:0}}>
        <div style={{width:30,height:30,borderRadius:"50%",flexShrink:0,background:tem?C.coral:C.cinzaL,color:C.branco,fontSize:13,fontWeight:800,display:"flex",alignItems:"center",justifyContent:"center",transition:"background 0.2s"}}>{idx+1}</div>
        <input type="text" value={dados.nome||""} onChange={e=>u("nome",e.target.value)}
          placeholder={`Nome da criança ${idx+1} (opcional)`}
          style={{flex:1,fontFamily:"inherit",fontSize:14,fontWeight:600,color:C.cinza,background:"transparent",border:"none",borderBottom:`1.5px solid ${C.cinzaL}`,padding:"3px 4px",outline:"none"}} />
        {onRemover && (
          <button onClick={onRemover} style={{background:"none",border:"none",color:C.cinzaM,fontSize:18,cursor:"pointer",padding:"0 4px",lineHeight:1}} title="Remover">×</button>
        )}
      </div>
      {tem && (
        <div style={{display:"grid",gap:7,marginTop:4}}>
          <textarea rows={2} value={dados.positivo||""} onChange={e=>u("positivo",e.target.value)}
            placeholder="✓  O que fez bem hoje..."
            style={{width:"100%",fontFamily:"inherit",fontSize:13,color:C.teal,border:`1.5px solid ${C.tealL}`,borderRadius:8,padding:"7px 10px",background:C.tealL,resize:"vertical",outline:"none",lineHeight:1.5}} />
          <textarea rows={1} value={dados.atencao||""} onChange={e=>u("atencao",e.target.value)}
            placeholder="⚠  Ponto de atenção..."
            style={{width:"100%",fontFamily:"inherit",fontSize:13,color:C.rosa,border:`1.5px solid ${C.rosaL}`,borderRadius:8,padding:"7px 10px",background:C.rosaL,resize:"vertical",outline:"none",lineHeight:1.5}} />
          <textarea rows={1} value={dados.orientacao||""} onChange={e=>u("orientacao",e.target.value)}
            placeholder="→  Orientação para a família..."
            style={{width:"100%",fontFamily:"inherit",fontSize:13,color:C.coral,border:`1.5px solid ${C.coralL}`,borderRadius:8,padding:"7px 10px",background:C.coralL,resize:"vertical",outline:"none",lineHeight:1.5}} />
        </div>
      )}
    </div>
  );
}

